import { listen } from "@tauri-apps/api/event";
import React, { useEffect, useRef, useState } from "react";
import { useTranslation } from "react-i18next";
import {
  MicrophoneIcon,
  TranscriptionIcon,
  CancelIcon,
  SendingIcon,
  ThinkingIcon,
} from "../components/icons";
import "./RecordingOverlay.css";
import { commands } from "@/bindings";
import { syncLanguageFromSettings } from "@/i18n";
import {
  ExtendedOverlayState,
  OverlayPayload,
  isExtendedPayload,
} from "./plus_overlay_states";

const RecordingOverlay: React.FC = () => {
  const { t } = useTranslation();
  const [isVisible, setIsVisible] = useState(false);
  const [state, setState] = useState<ExtendedOverlayState>("recording");
  const [errorMessage, setErrorMessage] = useState<string | null>(null);
  const [levels, setLevels] = useState<number[]>(Array(16).fill(0));
  const [profileName, setProfileName] = useState<string>("");
  const smoothedLevelsRef = useRef<number[]>(Array(16).fill(0));

  useEffect(() => {
    const setupEventListeners = async () => {
      // Listen for show-overlay event from Rust
      const unlistenShow = await listen("show-overlay", async (event) => {
        // Sync language from settings each time overlay is shown
        await syncLanguageFromSettings();
        
        const payload = event.payload;
        // Handle both extended payload objects and legacy string payloads
        if (isExtendedPayload(payload)) {
          setState(payload.state);
          if (payload.state === "error" && payload.error_message) {
            setErrorMessage(payload.error_message);
          } else {
            setErrorMessage(null);
          }
        } else {
          // Legacy string payload (e.g., "recording" or "transcribing")
          setState(payload as ExtendedOverlayState);
          setErrorMessage(null);
        }
        setIsVisible(true);
      });

      // Listen for profile switch event
      const unlistenProfileSwitch = await listen<string>("show-profile-switch", (event) => {
        setProfileName(event.payload);
        setState("profile_switch");
        setIsVisible(true);
      });

      // Listen for hide-overlay event from Rust
      const unlistenHide = await listen("hide-overlay", () => {
        setIsVisible(false);
      });

      // Listen for mic-level updates
      const unlistenLevel = await listen<number[]>("mic-level", (event) => {
        const newLevels = event.payload as number[];

        // Apply smoothing to reduce jitter
        const smoothed = smoothedLevelsRef.current.map((prev, i) => {
          const target = newLevels[i] || 0;
          return prev * 0.7 + target * 0.3; // Smooth transition
        });

        smoothedLevelsRef.current = smoothed;
        setLevels(smoothed.slice(0, 9));
      });

      // Cleanup function
      return () => {
        unlistenShow();
        unlistenProfileSwitch();
        unlistenHide();
        unlistenLevel();
      };
    };

    setupEventListeners();
  }, []);

  const getIcon = () => {
    switch (state) {
      case "recording":
        return <MicrophoneIcon />;
      case "sending":
        return <SendingIcon />;
      case "thinking":
        return <ThinkingIcon />;
      case "error":
        return <span className="overlay-icon-emoji">‚ùå</span>;
      case "profile_switch":
        return <TranscriptionIcon />;
      case "transcribing":
      default:
        return <TranscriptionIcon />;
    }
  };

  return (
    <div className={`recording-overlay ${isVisible ? "fade-in" : ""} ${state === "error" ? "overlay-error" : ""}`}>
      <div className="overlay-left">{getIcon()}</div>

      <div className="overlay-middle">
        {state === "recording" && (
          <div className="bars-container">
            {levels.map((v, i) => (
              <div
                key={i}
                className="bar"
                style={{
                  height: `${Math.min(20, 4 + Math.pow(v, 0.7) * 16)}px`, // Cap at 20px max height
                  transition: "height 60ms ease-out, opacity 120ms ease-out",
                  opacity: Math.max(0.2, v * 1.7), // Minimum opacity for visibility
                }}
              />
            ))}
          </div>
        )}
        {state === "sending" && (
          <div className="sending-text">Sending...</div>
        )}
        {state === "thinking" && (
          <div className="thinking-text">Thinking...</div>
        )}
        {state === "transcribing" && (
          <div className="transcribing-text">{t("overlay.transcribing")}</div>
        )}
        {state === "error" && (
          <div className="error-text">{errorMessage || "Failed"}</div>
        )}
        {state === "profile_switch" && (
          <div className="transcribing-text">{profileName}</div>
        )}
      </div>

      <div className="overlay-right">
        {/* Show cancel button for: recording, sending, thinking */}
        {(state === "recording" || state === "sending" || state === "thinking") && (
          <div
            className="cancel-button"
            onClick={() => {
              commands.cancelOperation();
            }}
          >
            <CancelIcon />
          </div>
        )}
      </div>
    </div>
  );
};

export default RecordingOverlay;
